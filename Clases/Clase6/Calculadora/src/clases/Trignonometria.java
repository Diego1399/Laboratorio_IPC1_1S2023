public class Trignonometria {
    
    
    public double seno(double a) {
        return Math.sin(a);
    }
    
    public double coseno(double a) {
        return Math.cos(a);
    }
    
    public double tano(double a) {
        return Math.tan(a);
    }
    
    
}

